// Variable : 

// var, let, const

/*
var : 
- gobal scope.
- function scope.
- can be re-declared.
- can be updated/re-assigned.
*/
var a = 10;
// console.log(a);
a = 20;
console.log(a);

var a = true;
console.log(a);

/*let : 
- block scope.
- cannot be re-declared.
- can be updated/re-assigned.
*/
let b = 10;
console.log(b);
b = 20;  // reassign
console.log(b);
// let b = true; // cannot be re-declared
// console.log(b);

/*const :
- block scope.
- cannot be re-declared.
- cannot be updated/re-assigned.
*/

const c = 10;
console.log(c);
// c = 20; // cannot be re-assigned
// console.log(c);
